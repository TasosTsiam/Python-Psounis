#Δένδρο(Ξανά)
#Ξαναλύστε την άσκηση για την εκτύπωση του δένδρου,
#αξιοποιώντας τον τελεστή * πάνω σε συμβολοσειρές:
#Υπενθύμιση για N=5: και δείχνει το δέντρο με 5 σειρές και τα αντίστοιχα αστέρια.

N = 5
for i in range(N):
    print("  " * (N - i - 1), end="")
    print("* " * (2 * i + 1), end="")
    print("")
