v1 = int(input("Enter your number: "))
v2 = int(input("Enter your number: "))
if v1 == v2:
    print(str(v1) + "==" + str(v2))
if v1 != v2:
    print(str(v1) + "!=" + str(v2))
if v1 < v2:
    print(str(v1) + "<" + str(v2))
if v1 > v2:
    print(str(v1) + ">" + str(v2))
if v1 >= v2:
    print(str(v1) + ">=" + str(v2))
if v1 <= v2:
    print(str(v1) + "<=" + str(v2))
